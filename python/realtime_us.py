#realtimecv + ultrasonic + uart at the same time.

from imutils.video import VideoStream
from pyzbar.pyzbar import decode
import RPi.GPIO as GPIO
# from multiprocessing import Process
# import threading
from threading import Thread
import argparse
import datetime
import imutils
import time
import cv2

#arguments
ap = argparse.ArgumentParser()
ap.add_argument("-o", "--output", type=str, default="barcodes.csv", help="path to output CSV file containing barcodes")
args = vars(ap.parse_args())

print("[INFO] starting video stream...")

# for using usb webcam
# vs = VideoStream(src=0).start()
# for using pi cam
vs = VideoStream(userPiCamera=True).start()
time.sleep(2.0)

csv = open(args["output"], "w")
found = set()

print("[INFO] Initiating the ultrasonic sensor")
GPIO.setmode(GPIO.BCM)

GPIO_TRIGGER = 18
#echo pin require exactly 3.3V
GPIO_ECHO = 24

GPIO.setup(GPIO_TRIGGER, GPIO.OUT)
GPIO.setup(GPIO_ECHO, GPIO.IN)

def distance():
    GPIO.output(GPIO_TRIGGER,True)

    time.sleep(0.00001)
    GPIO.output(GPIO_TRIGGER,False)

    StartTime = time.time()
    StopTime = time.time()

    while GPIO.input(GPIO_ECHO) == 0:
        StartTime = time.time()

    while GPIO.input(GPIO_ECHO) == 1:
        StopTime = time.time()

    TimeElapsed = StopTime - StartTime
    distance = (TimeElapsed * 34300)/2

    return distance

#cam thread loop
def threadA():
    while True:
        frame = vs.read()
        frame = imutils.resize(frame, width=400)

        barcodes = decode(frame)

        for barcode in barcodes:
            (x,y,w,h) = barcode.rect
            cv2.rectangle(frame,(x,y),(x+w,y+h),(0,0,255),2)

            barcodeData = barcode.data.decode("utf-8")
            barcodeType = barcode.type

            text = "{} ({})".format(barcodeData,barcodeType)
            cv2.putText(frame,text,(x,y-10), cv2.FONT_HERSHEY_SIMPLEX, 0.5, (0,0,255),2)

            if barcodeData not in found:
                csv.write("{},{}\n".format(datetime.datetime.now(), barcodeData))
                csv.flush()
                found.add(barcodeData)
        cv2.imshow("Barcode Scanner", frame)
        key = cv2.waitKey(1) & 0xFF

        if key == ord("q"):
            break

# ultrasonic thread loop
def threadB():
    try:
        while True:
            dist = distance()
            print("Measured Distance = %.1f cm" % dist)
            time.sleep(1)
    except KeyboardInterrupt:
        print("Measurement stopped by User")
        GPIO.cleanup()

if __name__ == "__main__":
    t1=Thread(target = threadA)
    t2=Thread(target = threadB)
    t1.setDaemon(True)
    t2.setDaemon(True)
    t1.start()
    t2.start()
    while True():
        pass

print("[INFO] cleaning up...")
csv.close()
cv2.destroyAllWindows()
vs.stop()